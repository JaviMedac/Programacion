        int respuesta = JOptionPane.showOptionDialog(this,
                "Si vuelve para atrás, se reseteará la partida cuando vuelvas a entrar, ¿estas seguro?",
                "Pregunta",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opciones,
                opciones[0]);
