    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int puntosD = Integer.parseInt(jTextField1.getText());
        puntosD = puntosD + 2;
        String resultado = String.valueOf(puntosD);
        random = (int) (Math.random() * 20);
        if (random < 10) {
            jTextField1.setText(resultado);
        }
        jProgressBar1.setValue(jProgressBar1.getValue() + 3);
        if (jProgressBar1.getValue() == 100) {
            jButton2.setEnabled(false);
            jButton5.setEnabled(false);
        }
        if (jProgressBar2.getValue() == 100 && jProgressBar1.getValue() == 100) {
            BasketApp ventana1 = new BasketApp(this, jTextField1.getText(), jTextField2.getText());
            setVisible(false);
            ventana1.setVisible(true);
            if (parseInt(jTextField1.getText()) > parseInt(jTextField2.getText())) {
                String mensaje = "Enhorabuena " + jLabel1.getText() + ", has ganado";
                JOptionPane.showMessageDialog(this, mensaje);

            } else if (parseInt(jTextField1.getText()) < parseInt(jTextField2.getText())) {
                String mensaje = "Enhorabuena " + jLabel2.getText() + ", has ganado";
                JOptionPane.showMessageDialog(this, mensaje);
            } else {
                String mensaje = "Oh, ha sido un empate";
                JOptionPane.showMessageDialog(this, mensaje);
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

